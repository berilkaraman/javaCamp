public class Main {
    public static void main(String[] args) {

        for(int i=1; i<10; i++){
            System.out.println(i);

       }
        System.out.println("for döngü bitti");
        //  while
        int i=1;
        while (i<10){
            System.out.println(i);
            i+=2;

        }
        System.out.println("while döngü bitti");

        //Do-While
        int j=1;
        do {
            System.out.println(j);
            j+=2;
        }while (j<10);
        System.out.println("do-while döngü bitti");
        }
    }
